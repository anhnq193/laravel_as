    {
        $now = Carbon::now();
        $stuPresent = json_decode($request ->get('stuPresent'));
        foreach ($stuPresent as $key => $value) {
            DB::table('attendance_details')->where('student_id', $value)->update(['status' => 1, 'created_at' => $now]);
        }
        return response() ->json(['result' => true]);
